# String Slicing in Python 

# string[start : stop : step]

text = "Hello World !"

print(text[0:5]) # Hello

print(text[:5]) # Hello (start from the beginning)

print(text[7:]) # world (go to end)
 
print(text[::-1])  # DLROWOLLEH  Reverse is printed in this 

